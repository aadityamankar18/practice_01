package java_practice.java_Practice_01;

public class c_arithmetic_Operators {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        // Addition
        int sum = a + b;

        // Subtraction
        int difference = a - b;

        // Multiplication
        int product = a * b;

        // Division
        int quotient = a / b;

        // Modulus (Remainder)
        int remainder = a % b;

        // Increment and Decrement
        a++; // Increment a by 1 (a becomes 11)
        b--; // Decrement b by 1 (b becomes 4)
//        System.out.println("After Incrementing a: " + a); // Output: 11
//        System.out.println("After Decrementing b: " + b); // Output: 4

        long w = 438347634;
        long i = w * 24233;    //alt+enter to import the class
//        System.out.println(i);

        double m = 10;
        int n = 3;
        double result = m / n;
        System.out.println(result);


        int A = 10;
        A += 2;  //compound assignment operator
        System.out.println(A);

        //increment and decrement operators
        int x = 5;
        System.out.println(x++); // prints 5, then x becomes 6
        System.out.println(x);   // prints 6

    }
}



























